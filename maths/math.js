function max(a, b){
    return a > b ? a: b;
}

/**
 * @param {number} number 
 * @returns ceil
 */
function ceil(number){
 return Math.ceil(number)
}

/**
 * @param {angle} angle 
 * @returns {number} return tan 
 */
function tan(angle){
    return Math.tan(angle);
}

/**
 * @param {angle} angle 
 * @returns {number} returns cos 
 */
function cos(angle){
    return Math.cos(angle);
}

